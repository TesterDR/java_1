public class Book2 {
    static class Main {
    public static void main(String[] args) {
        int x = 3;
        double d = 3.1;
        System.out.println(x == d); // false
        System.out.println(x > d);  // false
        System.out.println(x < d);  // true
        System.out.println(x == d);
        System.out.println(x == d);
    }
}
}
